$(function() {
    $(".tabmenu1 li").click(function() {
        var num = $(".tabmenu1 li").index(this);
        $(".tabContent1").removeClass('active');
        $(".tabContent1").eq(num).addClass('active');
        $(".tabmenu1 li").removeClass('active');
        $(this).addClass('active')
    });
});
